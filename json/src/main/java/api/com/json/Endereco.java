package api.com.json;

public class Endereco {

	String logradouro;
	String bairro;
	String localidade;
	String ufLoc;

	public String getLogradouro() {
		return logradouro;
	}

	public String getBairro() {
		return bairro;
	}

	public String getLocalidade() {
		return localidade;
	}
	

	public String getUf() {
		return ufLoc;
	}

	public void setUf(String uf) {
		this.ufLoc = uf;
	}

	@Override
	public String toString() {
		return "Endereco{" + "logradouro=" + logradouro + ", bairro=" + bairro + ", localidade=" + localidade + ",uf=" + ufLoc + '}';
	}
}
